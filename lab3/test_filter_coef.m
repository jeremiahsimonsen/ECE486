b1 = [1, 2, 1];
b2 = [1, -2, 1];
b3 = [1, 0.851559, 1];
b4 = [1, -1.90211, 1];
b5 = [1, 1.17557, 1];

a1 = [1, 2, 1];
a2 = [1, -2, 1];
a3 = [1, 0.851559, 1];
a4 = [1, -1.90211, 1];
a5 = [1, 1.17557, 1];

g = 0.0264722;

x = rand(1,1001);

y1 = filter(g*b1,a1,x);
y2 = filter(b2,a2,y1);
y3 = filter(b3,a3,y2);
y4 = filter(b4,a4,y3);
y5 = filter(b5,a5,y4);

Hf = fft(y5./x);
f = linspace(0,0.5,length(x));
plot(f,20*log10(abs(Hf)));